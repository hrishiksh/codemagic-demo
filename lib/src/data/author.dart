class Author {
  final String id;
  final String name;
  final String slug;
  final String bio;
  final String description;
  final int quoteCount;
  final String link;
  final String dateAdded;
  final String dateModified;

  const Author({
    required this.id,
    required this.name,
    required this.slug,
    required this.bio,
    required this.description,
    required this.quoteCount,
    required this.link,
    required this.dateAdded,
    required this.dateModified,
  });
}
